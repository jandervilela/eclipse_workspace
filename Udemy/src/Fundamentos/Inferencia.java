package Fundamentos;

public class Inferencia {
	public static void main(String[] args) {
		
		double a = 4.5;
		System.out.println(a);
		
		var b = 4.6;
		System.out.println(b);
		
		var c = "Texto";
		System.out.println(c);
		
		c = "outro Texto";
		System.out.println(c);
		
		double d;
		d = 123.65;
		System.out.println(d);
	}
}
