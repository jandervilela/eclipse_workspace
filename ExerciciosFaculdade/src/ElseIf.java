
public class ElseIf {
	
	public static void main(String[] args) {
		
		int x = 3;
		if (x==1) {
			System.out.println("x é igual 1");
		}
		else if(x==2) {
			System.out.println("x é igual 2");
		}
		else if(x==3) {
			System.out.println("x é igual 3");
		}
		else {
			System.out.println("nenhuma das alternativas");
		}
	}

}
